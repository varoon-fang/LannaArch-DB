                                    <h3>ประเพณีจิบอกไฟ (จุดบ้องไฟ)</h3>
                                      
                                     <p>ประเพณีจุดบ้องไฟ ซึ่งชาวล้านนาเรียกว่า “จิบอกไฟ” นี้ นิยมเล่นกันทั่วภาคเหนือในเทศกาลต่างๆ เช่น งานประเพณีสรงน้ำพระ เช่น วัดพระนอนขอนม่วง อำเภอแม่ริม เชียงใหม่ วัดพระนอนป่าเก็ดถี่ อำเภอสารภี เชียงใหม่ และวัดพระบรมธาตุสุเทพ เป็นต้น ประชาชนนิยมทำบอกไฟมาจุดเพื่อเป็นพุทธบูชา เพื่อความสนุกสนาน และเพื่อได้บุญกุศล เพราะเป็นความตั้งใจจะสักการะ กราบไหว้ พระเกษแก้วจุฬามณีบนสวรรค์ชั้นดาวดึงส์ ประเพณีจุดบอกไฟแต่และแห่ง จัดเป็นงานใหญ่ทุกปี มีการประกวดชิงรางวัลจากคณะกรรมการจัดงาน มีผู้สนใจทำบอกไฟเข้าแข่งขันเป็นจำนวนมาก จะเริ่มจุดบอกไฟตั้งแต่เช้า เวลา 9.00 น. จนถึงค่ำ ประเพณีสำหรับวัดต่างๆ แตกต่างกันไป เช่นประเพณีจิบอกไฟวัดพระนอนขอนม่วง กำหนดเอาเดือน 9 เหนือ แรม 14-15 ค่ำ รวม 2 วัน ส่วนวัดพระนอนป่าเก็ดถี่ กำหนดเอาสงกรานต์ปีใหม่ วันพญาวัน (เถลิงศก) วัดพระบรมธาตุสุเทพ กำหนดเอาวันเพ็ญ เดือน 8 เหนือ ส่วนวัดวาอารามในจังหวัดอื่นๆ ก็จุดกันตามประเพณีที่กำหนดแต่ละแห่ง ซึ่งไม่เหมือนกัน<br />
                                     ก่อนที่จะนำบอกไฟไปจุด แต่ละวัดจะพากันไปทั้งพระสงฆ์และประชาชนที่ทำบุญในวัดนั้นๆ โดยจะแห่แหนกันไปอย่างสนุกสนาน ไปยังสถานที่ที่เตรียมไว้ ซึ่งจะต้องห่างจากหมู่บ้าน เพราะเกรงจะเกิดอันตรายจากการระเบิดของบ้องไฟบางชนิดที่ทำผิดสูตร หรือตกใส่หลังคาบ้าน เมื่อแห่ไปถึงสถานที่ที่เตรียมไว้ เขาจะทำเป็นร้านสำหรับจุดบอกไฟ ซึ่งเรียกกันว่า “ค้าง” ร้านหรือค้างจะสูงกว่ายอดไม้ เวลาจะจุดเขาจะเอาบอกไฟไปวางไว้บนค้างโดยหันหัวขึ้นใช้มุมเอียง 45 องศา หรือแล้วแต่สล่าจิบอกไฟ คือ ช่างที่จุดจะเห็นสมควร เวลาจุดบอกไฟดินปืนซึ่งเรียกกันว่า “เฝ่า” จะดันบอกไฟพุ่งขึ้นไปอย่างสูงลิ่ว เมื่อหมดแรงดันก็จะตกลงมา</p>
                         
