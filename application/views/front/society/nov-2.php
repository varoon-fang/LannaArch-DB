                                                <p>ประเพณีเดือนยี่เพ็ง<br />
                                                พอเริ่มขึ้นหนึ่งค่ำเดือนยี่ ทุดวัดวาอารามในภาคเหนือจะจัดเตรียมสถานที่ปัดกวาดวิหารศาลาให้สะอาดงดงาม แล้วจัดเตรียมสิ่งต่อไปนี้<br />
                                                1.	ทำราชวัตรรอบวิหารหรือศาลา และทำประตูป่าหน้าทางเข้าวัด หรือหน้าวิหาร<br />
                                                2.	ทำโคมค้าง โคมแขวน โคมหูกระต่าย โคมรังมดส้ม (มดแดง) โคมรูปต่างๆ บางรายทำเป็นรูปเครื่องบิน ทำโคมผัดหรือโคมเวียน<br />
                                                3. ทำว่าวหรือโคมลอย โดยการนำกระดาษมาต่อกันเป็นทรงกลม ทำส่วนหัวและปากโคมลอยให้มีสัดส่วนเท่ากัน ไม่เช่นนั้นโคมลอยจะไม่ลอยขึ้นไป ลักษณะโคมลอยมีหลายแบบ เช่น แบบลูกฟัก แบบกล่องหรือกระติ๊บข้าว แบบรังมดส้ม แล้วแต่ช่างผู้ทำจะเห็นอย่างไร<br />
                                                โคมลอยมีลักษณะการปล่อยเป็น 2 อย่าง คือ โคมที่ใช้ปล่อยตอนกลางวันเรียกว่า ว่าว จะใช้การรมควัน คือ เพิ่มควันเข้าไปในตัวโคมลอยหรือว่าวเรื่อยๆ จนพองตัวมีความดันสูงขึ้นจนตึงมือ แล้วปล่อยขึ้นสู่ท้องฟ้า<br />
                                                โคมลอยที่ใช้ปล่อยตอนกลางคืน มีกรรมวิธีเช่นเดียวกันกับโคมว่าวตอนกลางวัน แต่แตกต่างกันที่เขาใช้ท่อนไม้ฟันด้ายเป็นก้อนกลมๆ ชุบด้วยน้ำมันยางหรือน้ำมันขี้โล้อ จนชุ่มแล้วทำที่แขวนติดกับปากโคมลอยเมื่อรมควันจนได้ที่แล้ว เขาจะจุดไฟท่อนผ้าที่เตรียมไว้แล้วผูกติดกับปากโคมลอย ปล่อยขึ้นสู่อากาศ โคมลอยจะลอยสูงขึ้นเรื่อยๆ และลอยไปตามกระแสลมจะมีลักษณะเป็นดวงไฟ คล้ายดาวเคลื่อนคล้อยไปในเวหาอันเวิ้งว้างน่าดูยิ่งนัก แต่โคมลอยที่ปล่อยกลางคืนนี้เป็นอันตรายต่อทรัพย์สินของผู้อื่นมาก บางครั้งผ้าที่ชุบน้ำมันยังไม่หมดเชื้อ ปรากฎว่าว่าวตกลงมาก่อนจึงเกิดไหม้บ้านเรือน หรือย่านป่าที่แห้งจัด เกิดไฟไหม้ป่าลุกลามไป ดังนั้นโคมลอยที่ใช้ปล่อยตอนกลางคืนจึงขาดความนิยมไป<br />
                                                4. การท่ำบอกไฟ ประกอบด้วยบอกไฟหลายชนิด เช่น บอกไฟดอก (ดอกไม้ไฟ) บอกไฟดาว (พลุ) บอกไฟเทียน บอกไฟขวี (เทียนเล็ก) บอกไฟช้างร้อย บอกไฟจักจ่า บอกไฟท้องตัน บอกไฟขี้หนู งานตำราประตา (ปรอท) เป็นคู่มือในการผสมดินประสิว กำมะถัน (มาด) และถ่านให้ถูกส่วนกัน ทำบอกไฟแต่ละชนิดขึ้นจุดในวันประเพณีเดือนยี่<br /></p>
                         
