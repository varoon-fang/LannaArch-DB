                                    <h3>ประเพณียี่เป็ง</h3>
                                      
                                                <p>ประเพณี เดือนยี่ หรือบางทีเรียกกันว่า เดือนยี่เป็ง นับเป็นประเพณีเก่าแก่ของล้านนาไทย ประชาชนนับถือปฎิบัติกันมานับแต่พุทธศตวรรษที่ 14 สมัยอาณาจักรหริภุญชัยได้มีประเพณีเดือนยี่และทำพิธีลอยโขมดแล้ว ในตำนวนเมืองลำพูนกล่าวว่า ชาวเมืองหริภุญชัยได้อพยพหนีอหิวาตกโรคไปอยู่เมืองหงสาวดี ซึ่งมีเชื้อสายมอญด้วยกัน เป็นเวลาหลายปี เมื่อโรคร้ายสงบแล้ว ชาวเมืองลำพูนบางคนก็ย้ายกลับบ้านเมืองเดิมของตน บางคนก็แต่งงานมีครอบครัว เมื่อถึงเดือนยี่เป็งมาถึง ชาวเมืองลำพูนคิดถึงญาติที่อยู่ ณ.เมืองหงสาวดี ก็จะเอาวัตถุข้าวของใส่แพไหลล่องไปตามลำน้ำแม่กวง แม่ทา แม่ปิง โดยคิดว่าสิ่งของเหล่านั้นจะล่องลอยไปถึงญาติที่อยู่เมืองหงสาวดีโพ้น จึงเป็นต้นเหตุแห่งการลอยโขมด หรือลอยกระทงแต่นั้น และประเพณีเดือนยี่ก็มีมาแต่ครั้งกระโน้นแล้ว</p>
                                                <p>ในหนังสือโบราณล้านนาหลายฉบับกล่าวถึงเดือนยี่เป็ง เช่น คำกาพย์ลำดับกัณฑ์มหาชาติกล่าวถึงเดือนยี่เป็ง ตอนหนึ่งว่า “เวสสันตรปริปุณณัง มาเถิงนครกัณฑ์ สุดซ้อยหล้าเดือนยี่เป็ง พระจันทร์เพ็งเปล่งผ้ามณฑลทั่วฟ้า สุกใสวาดยำยวา บัดนี้ข้าจักได้ลำดับลำดากัณฑ์มหาชาติแก้ว หื้อเป็นที่แล้วแห่งนิคมธรรม ฯลฯ”</p>
                                                <p>สมัยอาณาจักรล้านนาไทย ระยะพุทธศักราช 2061 รัชกาลพระเจ้าดิลกปนัดดาธิราช (พระเมืองแก้ว) เทศกาลเดือนยี่เพ็ง เป็นประเพณีใหญ่ ประชาชนพลเมืองทั่วประเทศ มีมหากษัตริย์เป็นประมุข ต่างก็พากันไปนมัสการบูชาพระในอารามทั้งหลาย คราคร่ำไปด้วยผู้คนหนึ่มสาว สนุกสนานมีความสุข เพราะได้ดูชมมหรสพที่เล่นกันเป็นที่พึงพอใจ โคลงนิราศหริภุญชัยกล่าวถึงเดือนยี่เพ็งไว้หลายตอน อันแสดงถึงความนิยมชมชอบของผู้คนสมัยโน้น กวีผู้แต่งตึงนำมาสอดแทรกไว้ในนิราศของตน<br />
