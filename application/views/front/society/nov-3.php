                                           <p> ใครเกิดปีใด เดือนใด ปีใด ก็เลือกสร้างหรือบูชา (เช่า) ธรรมคัมภีร์นั้นๆนำไปไว้ที่บ้านตระเตรียมทำกัณฑ์เทศน์ตามแต่อัธยาศัย    ใครจะทำบุญอุทิศให้แก่ใครก็เขียนใส่สะเรียงคือกระดาษตัดเป็นรูปสามเหลี่ยมด้านไม่เท่ามีปลายแหลม    แล้วเอาพันปลายไม้ที่เหลาไว้ปักบนกัณฑ์เทศน์</span></p>
                                           <p><strong>กำหนดวันฟังเทศน์หรือตั้งธรรม</strong><br />
                                           มีคนเอากัณฑ์เทศน์หรือธรรมมาก ทางวัดก็จะกำหนดวันที่จะฟังเทศน์กัณฑ์นั้นๆถ้ามีผู้เป็นศรัทธากัณฑ์เทศน์ มหาชาติมากๆก็จะกำหนดฟังมหาชาติเป็นวันๆ เช่น 2 วัน 3 วันเป็น ต้น นอกนั้นฟังธรรมวัตรคือธรรมคัมภีร์ที่ไม่ใช่มหาชาติ สลับวันกับการฟังเทศน์มหาชาติ<br />
                                           เมื่อกำหนดวันแล้วก็จะนำคัมภีร์นั้นๆไปตกหัววัดที่เคยทำบุญถึงกัน วัดละกัณฑ์ ส่วนมหาชาตินั้นถ้าต้องการจะฟังพระองค์ใดที่ไหน    ก็ไปนิมนต์ท่านถึงวัดแม้หัววัดจะไม่เคยถึงกันก็ตาม กำหนดนิมนต์ให้ท่านมาเทศน์ในวันที่กำหนดไว้    ส่วนมากการฟังเทศน์มหาชาติจะมีกี่วันก็ตามแต่ต้องฟังในวันสุดท้ายด้วย</span></p>
                                           <p><strong>การประดับตกแต่งสถานที่</strong><br />
                                           ในการฟังเทศน์ทุกวันจะมีการเวนทานโดยอาจารย์ของวัดทุกวัด ส่วนวันใดมีการเวนทานธรรมคัมภีร์เวสสันดรชาดก    ก็จะเวนทานมหาชาติซึ่งนักปราชญ์โบราณได้แต่งไว้โดยเฉพาะ เป็นประเพณีสืบต่อกันมา    ในปัจจุบันนี้การตั้งธรรมหรือฟังเทศน์มหาชาติไม่ค่อยได้ทำตามแบบตามที่โบราณาจารย์กำหนดไว้    การสร้างธรรมคัมภีร์ตามชาตาปี เดือน วันเกิดของตน ก็ไม่ค่อยจะมีใครปฏิบัติตามเท่าไรนัก    อาจเป็นเพราะไม่ทราบหรือเพราะประเพณีทางภาคกลางเข้ามาครอบงำจนไม่ทราบประเพณีอันแท้จริงของล้านนาไทย    ประกอบกับทางการก็ไม่สนับสนุนฟื้นฟู สิ่งที่นับวันจะหายไป คือ พระธรรมคัมภีร์ทั้งหลายจะน้อยลง    พระนักเทศน์ทำนองมหาชาติล้านนาไทยก็จะหมดไป ปัจจุบันมีเหลืออยู่ไม่กี่รูป ส่วนมากก็มีอายุเกิน    75 ปีแล้ว ถึงแม้จะจัดให้มีการฟังเทศน์มหาชาติในปัจจุบัน ก็เลือกฟังเฉพาะกัณฑ์    ไม่ได้ฟังครบทั้ง 13 กัณฑ์ กัณฑ์ดังกล่าวและฟังกันเพื่อความสนุกสนานเหมือนฟังจำอวดไป<br />
