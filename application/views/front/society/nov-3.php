                                           <p>ในการเทศน์ตามกัณฑ์ที่กล่าวมานี้ ก่อนจะเทศน์พระผู้เทศน์จะใส่กาบเค้า คือแหล่กาพย์ตอนต้นจะเป็นเรื่องอะไรก็ได้ที่เข้ากับเหตุการณ์    เช่น กาบสิทธารถ กาบพิมพาพิลาป กาบศีลห้า เป็นต้น แต่เสียงใหญ่มักนิยมใส่กาบคำสอน    เช่น กาบทศพิธราชธรรม กาบร่ำสงสาร เป็นต้น เฉพาะกัณฑ์ชูชก จะต้องใส่กาบเค้าเรื่องกำเนิดของชูชก    เมื่อจบกาบเค้าแล้วจะดำเนินเทศน์ตามเนื้อเรื่องที่มีในคัมภีร์</span></p>
                                           <p>เมื่อเทศน์จบในกัณฑ์ใดแล้ว จะมีการใส่กาบปลายอีกครั้งหนึ่ง เป็นการสรุปใจความในกัณฑ์นั้น    ถ้าเป็นกัณฑ์สุดท้ายคือนครกัณฑ์ มักจะใส่กาบลำดับกัณฑ์ คือสรุปเรื่องราวของมหาเวสสันดรทั้งหมด    ตั้งแต่กัณฑ์ทศพรถึงนครกัณฑ์</span></p>
                                           <p>การฟังเทศน์มหาชาตินี้ มีบางครั้งบางกัณฑ์เช่นกัณฑ์มัทรี กัณฑ์กุมาร กัณฑ์มหาราชมักจะนิมนต์พระที่เทศน์เก่งมาเทศน์ประชันกัน    การเทศน์ประชันกันนี้ ให้องค์หนึ่งเทศน์จนจบกัณฑ์แล้วองค์ที่ 2 จึงจะเทศน์ต่อ ไม่ได้เปลี่ยนกันแหล่สลับกันเหมือนภาคกลาง    มีการเทศน์ในกัณฑ์เดียวมากๆเวลาที่จะฟังให้จบในวันหนึ่งคืนหนึ่งก็น้อยลง บางครั้งเมื่อเทศน์ถึงนครกัณฑ์จวนจะจบแล้ว    พระอาทิตย์ในวันใหม่ก็จะโผล่พ้นขอบฟ้า ศรัทธาต้องเร่งปิดประตูวิหารเพื่อมิให้แสงสว่างอย่างนี้ก็มี</span></p>
                                           <p><strong>การตั้งธรรม</strong><br />
                                           เมื่อพูดถึงประเพณีการเทศน์มหาชาติ ก็ใคร่จะนำเอาประเพณีการตั้งธรรมมาพูดรวมไว้ในทีเดียวกัน    “การตั้งธรรม” ฟังชื่อแล้วรู้สึกแปลกเหมือนเอาพระธรรมคัมภีร์มาตั้งไว้ ความเป็นจริงหมายถึงการฟังเทศน์เป็นการใหญ่    คือฟังกัน 3 วันบ้าง 5 วันบ้าง 7 วันบ้าง 10 วันบ้างการตั้งธรรมเช่นนี้เป็นประเพณีนิยมสืบต่อกันมา    ตั้งแต่สมัยโบราณ</span></p>
                                           <p><strong>วัตถุประสงค์ </strong>เพื่อต้องการหาเงินมาสร้างสิ่งถาวรภายในวัดอย่างใดอย่างหนึ่ง    เพราะในสมัยโบราณนั้นยังไม่มีการทอดกฐิน หรือผ้าป่าบำรุงเหมือนปัจจุบัน</span></p>
                                           <p>เมื่อจะตั้งธรรมก็ประชุมศรัทธาชาวบ้านถามความเห็น เป็นการปรึกษาหารือกัน เมื่อศรัทธาส่วนมากเห็นชอบด้วย    ก็จะกำหนดเดือนวันที่จะตั้งธรรม ส่วนมากมักจะเป็นเดือนยี่ เดือน 3 เดือน 4 เดือน    5 เดือน 6 เหนือ นอกจากเดือนเหล่านี้แล้วไม่นิยมกัน</span></p>
                                           <p><strong>ธรรมหรือคัมภีร์ที่ใช้เทศน์</strong><br />
                                           เมื่อตกลงกันแล้วก็จะเริ่มหาศรัทธาเป็นเจ้ากัณฑ์นั้นๆ ใครจะเป็นเจ้าภาพเรื่องอะไร    กัณฑ์ไหนก็มาแจ้งแก่เจ้าอาวาส ท่านจะจดชื่อไว้</span></p>
                                           <p><strong>นักปราชญ์ล้านนาไทยแต่โบราณ </strong>ท่านมีกุศโลบายอันฉลาดที่จะดึงให้ชาวบ้านรู้จักทำบุญท่านได้กำหนดชาดกธรรมประจำปีเกิด    เดือนเกิด วันเกิด ของคนทั้งหลายไว้ พร้อมกับบอกไว้ว่า”บุคคลใดก็ตาม” ถ้าริบรอมทรัพย์สมบัติใดไม่ขึ้น    เก็บเงินไม่อยู่มักทำให้สิ้นไปหมดไป ให้ทานธรรมชาตาของตนเสีย แล้วจะวุฒิจำเริญ” </span></p>
                                           <p>ปรากฏว่าคนทั้งหลายต่างก็ขวนขวายหาทางสร้างคัมภีร์ทั้งหลายอันเป็นธรรมประจำชาตาตนเอง    โดยจ้างคนที่เข้าใจจารคัมภีร์นั้นๆให้ แล้วนำมาถวายแก่สงฆ์ ด้วยเหตุนี้คัมภีร์ทั้งหลาย    ไม่ว่าจะเป็นมหาชาติหรือชาดกอื่นๆมักจะมีเป็นจำนวนมาก บางวัดเฉพาะมหาชาติมีเป็นตู้ๆปิฏกะทั้งสามมีเยอะทีเดียว    หากว่าไม่อาจจะสร้างใหม่ ท่านก็อนุญาตให้บูชา (เช่า) เอาคัมภีร์ที่มีอยู่แล้วในวัดมาถวายทาน    ก็ชื่อว่าได้บุญได้อานิสงส์เช่นกัน</span></p>
