                                           *ในแต่ละกัณฑ์ที่พระสงฆ์จะเทศน์จะมีเนื้อเรื่องย่อซึ่งเป็นเรื่องราวของเวสสันดรชาดกที่พระพุทธเจ้าทรงแสดงไว้เป็นภาษาบาลีดังนี้</span></p>
                                           <p> กัณฑ์ที่ 1 ชื่อกัณฑ์ทศพร พรรณนาตอนที่พระนางผุสดีขอพรจากพระอินทร์ 10 ประการ    ก่อนที่จะจุติในโลกมนุษย์เป็นมารดาของพระเวสสันดร</span></p>
                                           <p> กัณฑ์ที่ 2 ชื่อกัณฑ์หิมพานต์ พรรณนาถึงจุติปฏิสนธิของพระเวสสันดร จนถึงทรงได้อภิเษกสมรสกับพระนางมัทรี    พระธิดาแห่งกษัตริย์แคว้นมัทราชจนกระทั่งถูกเนรเทศออกจากเมืองเข้าสู่ป่าพรรณนาถึงป่าหิมพานต์</span></p>
                                           <p> กัณฑ์ที่ 3 ชื่อทานกัณฑ์ พรรณนาสัตตสดกมหาทาน พระเวสสันดรสั่งเมืองและให้ทานรถเทียมด้วยม้าแก่ผู้ที่มาทูลขอ</span></p>
                                           <p> กัณฑ์ที่ 4 ชื่อกัณฑ์วนปเวศน์ พรรณนาถึงสี่กษัตริย์เสด็จถึงนครมาตุลราช พระยาเจตราฐ    เจ้าเมืองทูลขอให้ครองสมบัติ พระเวสสันดรไม่ทรงรับ</span></p>
                                           <p> กัณฑ์ที่ 5 ชื่อกัณฑ์ชูชก พรรณนาถึงพราหมณ์ชูชกขอทานจนได้นางอมิตตดาลูกสาวเพื่อนเป็นเมีย    เมียต้องการคนรับใช้ให้พราหมณ์ชูชกไปขอกัณหาและชาลีมาเป็นคนใช้ พราหมณ์จึงออกเดินทางไปสู่เขาวงกฏ    เจอพรานเจตบุตรผู้รักษาทางเข้าป่าหิมพานต์</span></p>
                                           <p> กัณฑ์ที่ 6 ชื่อกัณฑ์จุลพน พรานเจตบุตรถูกชูชกหลอกจึงบอกทางไปสู่เขาวงกฏ</span></p>
                                           <p> กัณฑ์ที่ 7 ชื่อกัณฑ์มหาพน พรรณนาถึงพราหมณ์ชูชกพบอจุตฤาษี ก็ชี้ทางไปสู่เขาวงกฏ    พรรณนาถึงป่าเขาลำเนาไพร</span></p>
                                           <p> กัณฑ์ที่ 8 ชื่อกัณฑ์กุมาร ชูชกไปถึงเขาวงกฏเพื่อขอกัณหาชาลี สองกุมารลงไปหลบในสระน้ำ    พระเวสสันดรเรียกขึ้นมามอบให้พราหมณ์ชูชก ชูชกทุบตีฉุดกระชากลากสองกุมารไป</span></p>
                                           <p> กัณฑ์ที่ 9 ชื่อกัณฑ์มัทรี พระนางมัทรีกลับจากหาผลไม้ในป่าพบสัตว์สามตัวนอนขวางทางจึงไม่พบลูกทั้งสอง    พอทราบความจริงก็เป็นลมสลบไป</span></p>
                                           <p> กัณฑ์ที่ 10 ชื่อกัณฑ์สักกบรรพ์ พระอินทร์แปลงกายมาทูลขอพระนางมัทรีจากพระเวสสันดรแล้วคืนให้    พระเวสสันดรทูลขอพร 8 ประการจากพระอินทร์</span></p>
                                           <p> กัณฑ์ที่ 11 ชื่อกัณฑ์มหาราช พราหมณ์ชูชกพา 2 กุมารหลงทางเข้าสู่เมืองสีพี พระเจ้าสัญชัยทรงไถ่กุมารคืนด้วยสิ่งของอย่างละ    100 พราหมณ์ชูชกรับประทานอาหารจนตายพระเจ้าสัญชัยเตรียมไปรับพระเวสสันดรและพระนางมัทรี</span></p>
                                           <p> กัณฑ์ที่ 12 ชื่อกัณฑ์ฉกษัตริย์ พรรณนาถึงหกกษัตริย์พบกัน เกิดความยินดีจนเศร้าโศกสลบไป    พระอินทร์บันดาลฝนโบกขรพรรณตกลงมาประพรมจนฟื้นคืนสติทั้งหมด</span></p>
                                           <p> กัณฑ์ที่ 13 ชื่อกัณฑ์นครกัณฑ์ (ขณะเทศน์จะมีการโปรยข้าวตอกข้าวสาร สมมติว่าพระอินทร์ได้บันดาลฝนแก้ว “รัตนธารา”) กัณฑ์สุดท้ายถือเป็นกัณฑ์ชัยมงคลพรรณนาถึงพระเวสสันดร พระนางมัทรี ลาจากเพศพรตฤาษีนิวัติคืนสู่พระนครครองเมืองสีพี พระอินทร์บันดาลห่าฝนสัตตรตนะธาราตกลงทั้งภูมิมณฑลโปรดให้ประชาชีทำบุญทำทาน ตลอดพระชนมชีพ</span></p>
                                           <p>เมื่อเทศน์จบกัณฑ์หนึ่งๆ จะมีการประโคมฆ้องกลองบูชาบนวิหารก็จะจุดประทัดบอกสัญญาณให้รู้ว่า“ธรรมจบ กัณฑ์หนึ่ง“แล้วคนอยู่ที่ไหนได้ยินเสียงกลองก็จะประนมมือไหว้มาทางวัดกล่าว คำว่า “สาธุ”<br />
