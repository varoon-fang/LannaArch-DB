                                    <h3>ประเพณีตั้งธรรมหลวง</h3>
                                      
                                           <p>ประเพณีการฟังเทศน์มหาชาติหรือประเพณีตั้งธรรมหลวง </span></p>
                                           <p>มหาชาติ คือชาติที่ยิ่งใหญ่เป็นชาติสุดท้ายของพระพุทธเจ้า ก่อนที่จะมาอุบัติตรัสรู้เป็นพระพุทธเจ้าในชาติปัจจุบัน    รวมอยู่ในพระเจ้าสิบชาติ แต่คนนิยมฟังกันเป็นประเพณีเฉพาะตอนเป็นมหาชาติคือเมื่อเสวยพระชาติเป็นพระเวสสันดรเท่านั้น    เพราะสังคายนาจารย์ทั้งหลายได้กล่าวอานิสงส์แห่งการสร้างหรือฟังมหาเวสสันดรชาดกไว้ว่า</span></p>
                                           <blockquote><p> </span>“สังคายนาจารย์เจ้าทั้งหลายกล่าวดั่งนี้แล้ว ก็กล่าวคาถามาว่า “ปูชา ปาเก เตชยนติ      ทุคคติ เตน ปุญญสส ปาเก “ ดังนี้ว่า อันว่าคนทั้งหลายฝูงใด ได้บูชามหาเวสสันดรชาตก      ผู้นั้นก็จะได้เป็นเจ้าพระยาในเมืองคน ยศประวารบ่จนมีมาก ชางม้าหากเนืองนันต์      มีกลองนันทเภรีเก้าพันลูก เปี๊ยะพิณผูกเก้าพันเสียง สัททะสำเนียงชมชื่น สนุกต้องตื่นทุกรวายตรีทิวา      ทาสีทาสามีมาก พร้อมอยู่แวดล้อมเฝ้าปฏิบัติ ทิพพสัมปัตติล้ำเลิศ ก็กลับเกิดมีตาม      เล้มเงินเล้มคำและเสื้อผ้า ทั้งช้างม้าและเปลือกเข้าสาร ก็จักมีตามปรารถนาทุกเมื่อ      จำเริญเชื่อมงคล ยถา ในกาลเมื่อใดพระศรีอริยเมตไตรยมาตรัสประยาและปัญญา เป็นพระพายหน้าบุญแก่กล้าก็จักได้หันหน้าท่านบ่สงสัย      เหตุได้เป็นปาเถยยะกับธัมม์เวสสันตระชาตกะ อันยกมาที่นี้แล้ว ก็จักเถิงเซิ้งเวียงแก้วยอดมหาเนรพาน      บ่อย่าชะแล”</span></p></blockquote>
                                           <p>คัดมาจากธรรมอานิสงฆ์สร้างมหาเวสสันตรชาดก ของล้านนาไทยซึ่งโบราณาจารย์ท่านได้แต่งไว้อีกประการหนึ่งท่านกล่าวว่า    “ บุคคลใดตั้งใจฟังธรรมมหาชาติจนจบ 13 กัณฑ์ ภายในวันหนึ่งคืนหนึ่ง ท่านว่าผู้นั้นจะวุฒิจำเริญด้วยสมบัตินานาประการในปัจจุบัน    และจะได้เกิดร่วมศาสนาพระศรีอริยเมตไตรยในอนาคต ไม่ต้องสงสัย</span></p>
                                           <p>ในคัมภีร์มาลัยสูตรก็กล่าวเป็นใจความว่า “เมื่อครั้งมหาเถรขึ้นไปนมัสการพระเกศแก้วจุฬามณีในชั้นดาวดึงษ์นั้น    ได้พบพระอริยเมตไตรยเทพบุตร ท่านก็ได้สั่งมหาเถรเจ้ามาว่า “ให้คนทั้งหลายฟังธรรมมหาชาติให้จบทั้ง    13 กัณฑ์ในวันเดียวคืนเดียว แล้วจะได้ร่วมกับศาสนาของเรา” ดังนี้ เมื่อพระมหาเถรเทพมาลัยกลับจากสวรรค์แล้ว    ก็นำเรื่องนี้มาบอกกับชาวโลกคนทั้งหลายได้ฟังก็เลยพากันฟังเทศน์มหาชาติจนถือเป็นประเพณีมา”</span></p>
                                           <p>เพราะความนิยมที่คนชอบชาดกเรื่องนี้มาก จึงเกิดมหาชาติล้านนาไทยสำนวนต่าง ๆ ขึ้นประมาณ    120 สำนวน ส่วนมากแต่งมาตั้งแต่สมัยพระเจ้ากือนา (พ.ศ. 1910 -1943) และแต่งในสมัยต่อ    ๆ มา จนถึง พ.ศ. 2300 ในสมัยพระเจ้ากาวิละต้นรัตนโกสินทร์ ได้มีนักปราชญ์ล้านนาไทยนำเอามหาชาติสำนวนเก่ามาเกลาใหม่    เช่นฉบับอินทร์ลงเหลา (เหลาแปลว่า เกลา) ฉบับพระยาพื้น เป็นต้น</span></p>
                                           <p><strong>การฟังเทศน์มหาชาติ</strong><br />
                                           การฟังเทศน์มหาชาติ นิยมฟังกันมาตั้งแต่สมัยโบราณมา โดยปฏิบัติตามคำสั่งของอริยเมตไตรยเทพบุตร    ให้จบในวันหนึ่งคืนหนึ่ง และแต่งเครื่องบูชาครบตามที่สั่งไว้ คือดอกบัวเผื่อน ดอกปีบ    ธูป เทียน ข้าวตอกให้ครบพัน เพื่อบูชาพันคาถาในเรื่องนี้พร้อมกับวาดรูปนี้เป็นกัณฑ์    ๆ ของเหล่านี้เตรียมวางไว้หน้าพระประธานในวิหาร ส่วนรูปช้างม้าที่เป็นของทานแห่งพระยาเวสสันดรนั้น    ส่วนมากทำด้วยแผ่นเงินตอกเป็นรูปนูนด้านหนึ่งมัดแขวนไว้ที่ค้างเหมือนฉัตร รูปภาพมัดนั้นแขวนไว้ตามผาผนัง    ส่วนดอกบัวเผื่อนนั้นจะลอยบนกระถางน้ำก็ได้ ส่วนดอกปีบทำเป็นแผง ๆใช้ดอกไม้แห้งทำถึงแม้ดอกบัวเผื่อนจะทำเช่นนี้ก็ได้<br />
                                           นอกจากสิ่งของที่กล่าวมาแล้ว เมื่อจะมีการฟังเทศน์มหาชาติ ซึ่งถือเป็นประเพณีสืบต่อกันมา    ท่านกำหนดเอาวันเดือนยี่ (เหนือ ) เพ็ญเป็นวันฟังเทศน์เรื่องนี้ อันเป็นจบประเพณีฟังในฤดูเข้าพรรษา    ตามที่กล่าวในประเพณีเข้าพรรษานั้น</span></p>
                                           <p>ก่อนจะถึงวันฟังเทศน์มหาชาติ ทุกวัดวาอารามจะเตรียมสถานที่ ทำซุ้มประตูวัดเรียกกันว่าซุ้มประตูป่า    สมมุติว่าเป็นประตูเข้าสู่ป่าหิมพานต์ อันเป็นด่านที่พรานเจตบุตรคอยเฝ้าระวังมิให้คนใดผ่านเข้าไปรบกวนพระเวสสันดร    ต่อจากประตูวัดเข้าไปจะทำราชวัตรปลูกกล้วยอ้อยประดับช่อทุงและฉัตร บริเวณลานวัดจะผังค้างโคมแขวน    โคมทำด้วยกระดาษ โครงทำด้วยไม้ไผ่หักเป็นเหลี่ยมเป็นมุม หุ้มรอบป้องกันมิให้ลมพัดไฟดับ    ด้านล่างของโคมจะใช้กระดาษตัดขนาดเท่าผ่ามือ ยาวเหมาะสมกับโคมนั้น ทำเป็นชายห้อยลงมาหลายชาย    เวลาแขวนอยู่บนค้างชายโคมนี้จะถูกลมพัดพริ้วน่าดูมาก หน้าวิหารจะมีโคมกระดาษรูปร่างแปลก    ๆ แขวนเป็นระยะ ถ้ามีโคมมากก็จะแขวนถึงในวิหาร ส่วนในวิหารนั้นประดับด้วยธงราวและเครื่องบูชามหาชาติ    โดยได้เขียนไว้เป็นกลอนในนิราศเดือนล้านนาไทย ดังต่อไปนี้<br />
