                                                   <p>ประเพณีอุทิศหาผู้ตาย บางทีเรียกว่า ประเพณีเดือน 12 เพ็ง ประชาชนในล้านนาไทยนิยมทำบุญ อุทิศส่วนกุศลไปให้บรรพบุรุษของตน คือ พ่อแม่ ปู่ย่า ตายาย ลุงป้า ญาติพี่น้องที่ล่วงลับไปแล้ว ประเพณีนี้เรียกกันในแต่ละท้องถิ่นก็มีชื่อแตกต่างกันไป บางจังหวัดในล้านนาเรียกประเพณีอุทิศะหาผีตายบ้าง เรียกประเวณีเดือนสิบสองบ้าง เรียกประเวณีปล่อยผีปล่อยเปรต ตรงกับของภาคกลางว่า &#8220;ตรุษสารท&#8221; ปักษ์ใต้ เรียกว่า ประเพณีเดือนสิบชิงเปรต และ ทางภาคอีสานก็คือ ประเพณีบุญข้าวประดับดิน ประเพณีที่กล่าวมานี้ โดยความหมายและจุดประสงค์เป็นอันเดียวกัน ต่างกันด้วยวิธีการทำตามจารีตประเพณีที่เคย ทำมาในท้องถิ่นของตน<br />
                                                   สำหรับการอุทิศหาผู้ตายของล้านนาไทย มีประเพณีสืบต่อกันมาเนื่องในการอุทิศกุศลแก่ญาติพี่น้อง ผู้ที่ตายไปแล้ว ถือกันว่าในวันเดือน 12 เหนือ ขึ้น 1 ค่ำถึงเดือนแรม 14 ค่ำนั้น พระยายมราชได้ปล่อยวิญญาณสัตว์ ผู้ตายกลับมาสู่เมืองมนุษย์เพื่อขอรับเอาส่วนบุญกุศลจากญาติพี่น้องลูกหลาน จะได้พ้นจากภาวะแห่งเปรตอสุรกายทั้งหลาย ดังนั้นการปฏิบัติประเพณีก็ด้วยความกตัญญู ต้องการให้บุคคลผู้เป็นที่รักพบกับความสุขในปรโลก จึงได้ทำกันสืบๆ มา</p>              
                                 
