                                   <h3>ประเพณีทอดผ้าป่า</h3>
                                     
                                        <p>ในล้านนาไทย เมื่อเดือนยี่มาถึงนิยมทอดผ้าป่าหรือ “ทานทอด” กันเกือบทั่วไป นับแต่โบราณมาบางคนทำการถวายกฐินแล้วเอาผ้าป่าไปทอดอีก เป็นผ้าป่าหางกฐิน บางคนจัดต้นผ้าป่าขึ้นมาเป็นการเฉพาะมีลักษณะผ้าบังสุกุลจีวรที่ปรากฎตาม ตำนานทางพุทธศาสนาว่า คราวหนึ่งพระอนุรุทธเถระ ผู้เป็นพระอรหันต์เที่ยวแสวงหาบังสุกุลจีวรตามแนวป่าและเดินเข้าไปในป่าช้า หวังใจว่าจะพบผ้าสักผืนหนึ่ง ขณะที่พระอนุรุทธรำพึงเช่นนั้น เทพธิดานางหนึ่ง ซึ่งชาติก่อนเคยเป็นภรรยาของพระอนุรุทธเถระ ผู้เป็นพระอรหันต์เที่ยวแสวงหาบังสุกุลจีวรตามแนวป่าและเดินเข้าไปในป่าช้า หวังใจว่าจะพบผ้าสักผืนหนึ่ง ขณะที่พระอนุรุทธรำพึง เช่นนั้น เทพธิดานางหนึ่ง ซึ่งชาติก่อนเคยเป็นภรรยาของพระอนุรุทธ มีศรัทธาอยากจะถวายผ้าบังสุกุลแก่ท่าน จึงนำเอาผ้าทิพย์ลงมาพาดไว้ที่ต้นไม้เตี้ย ๆ ต้นหนึ่ง ระหว่างทางที่พระเถระเดินผ่านไป เมื่อพบเหตุเช่นนั้นจึงแสดงความประหลาดใจ เมื่อถามถึงเจ้าของไม่มี ท่านจึงอธิษฐานเป็นผ้าบังสุกุล การทอดผ้าป่าสมัยต่อมาถึงปัจจุบัน ก็มาจากสาเหตุเดิมนี้เอง</p>
                                        <p>การทอดผ้าป่าปัจจุบัน เจ้าภาพจะนำเอากิ่งไม้ที่เหมาะแก่ต้นผ้าป่ามาใช้แทนต้นไม้ หรือพุ่มไม้โดยมากใช้กิ่งมะขาม เพราะมีกิ่งมากและหนียวด้วย นำมาปักติดกับต้นกัณฑ์สังเคต หรือ หม้อไหภาชนะ แล้วแต่ศรัทธา ภายในภาชนะดังกล่าวจะใส่เครื่องอุปโภค บริโภคของกินของใช้ เช่น ข้าวสาร ฟักแฟง ปลาย่าง ปลาแห้ง ผลไม้ต่าง ๆ และของใช้ภายในครัว ส่วนต้นผ้าป่าก็จะแขวนวัตถุที่ค่อนข้างเบา มีหมากพลู บุหรี่ ไม้ขีด เทียนไข สมุด ดินสอ เป็นต้น แล้วมี ผ้าจีวร หรือสบงที่ใช้เป็นผ้าป่าแขวน หรือพาดไว้ผืนหนึ่ง สำหรับให้พระสงฆ์มาพิจารณาชักบังสุกุล สิ่งที่นิยมทำกันเป็นประจำคือ การนำเอาผ้าขนหนูมาทำเป็นรูปชะนีเล็ก ๆ แขวนไว้ด้วย</p>       
                        
