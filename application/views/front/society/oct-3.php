                                    <h3>ประเพณีทอดกฐิน</h3>
                                      
                                        <p>ใน สมัยโบราณล้านนาไทยไม่ค่อยนิยมทอดกฐินกันอย่างในสมัยปัจจุบัน เนื่องมาจากการถวายกฐินเป็นงานใหญ่ ต้องตระเตรียมมาก โดยเฉพาะการเตรียมบริวารกฐินและการเลี้ยงดูญาติพี่น้องมิตรสหายที่มาร่วมงาน ผู้ถวายกฐินจะต้องเป็นคนมีฐานะดี มีเจตนาศรัทธาแรงกล้า จึงจะถวายได้ การทำบุญกฐินในสมัยโบราณจึงมีน้อย แต่การถวายกฐินนับเป็นประเพณีสำคัญอยู่ เมื่อผู้มีศรัทธาในหมู่บ้านใดผู้หนึ่ง มีเจตนาจะถวายกฐิน ประกาศให้รู้ด้วยการไปจองไว้ที่วัด จึงทำความชื่นชมยินดีแก่ชาวบ้านใกล้เรือนเคียง เมื่อคราวมีงาน จึงมาร่วมด้วยเป็นอันมาก</p>
                                        <p><strong>กฐินแปลว่าอะไร</strong><br />
                                        กฐิน แปลว่า ไม้สะถึง คือ กรอบไม้ชนิดหนึ่งใช้สำหนับขึงผ้าให้ตึงสะดวกแก่การตัดเย็บในสมัยโบราณเพราะ ช่างไม่มีความชำนาญ อีกทั้งผ้าไตรจีวรของพระภิกษุเป็นผ้าผืนเล็กบ้าง ใหญ่บ้าง ยากที่จะกำหนดกฎเกณฑ์ตายตัว การเย็บจีวรนั้นพระทั้งหลายก็จะมาช่วยกัน อุบาสก อุบาสิกาก็จัดหาน้ำดื่ม เป็นต้น มาถวายพระภิกษุสามเณร การเย็บจีวรนี้แม้เป็นเรื่องธรรมดาแต่ก็ต้องช่วยกันทำหลายคน<br />
                                        -	โดยความหมาย ผ้ากฐิน ก็คือ ผ้าที่สำเร็จรูปโดยอาศัยไม้สะดึง<br />
                                        -	ผู้ทอดกฐิน ก็คื ผู้นำกฐินไปถวายแก่สงฆ์<br />
                                        -	องค์ครองกฐิน ก็คือ ภิกษุผู้ได้รับผ้ากฐินฃ</p>
                                        <p><strong>เขตกำหนดการทอดกฐิน</strong><br />
                                        ครั้นออกพรรษาแล้ว ก็เป็นธรรมเนียมต้องอยู่เพื่อซ่อมผ้าไตรจีวรอีกเดือนหนึ่ง คือ ตั้งแต่แรม 1 ค่ำ เดือน 11 – ขึ้น 15 ค่ำ เดือน 12 เรียกว่า “จีวรกาล” ปกติจีวรของภิกษุเก่าบ้าง ใหม่บ้าง ดีบ้าง เลงบ้างแล้วแต่จะให้ เมื่อเปื่อยขาดก็จำเป็นหาใหม่ ทำใหม่ ดังนั้นพระพุทธองค์จึงให้อยู่ทำจีวรในอาวาสที่มีพระมาก มีพระประสงค์เพื่อจะให้ช่วยเหลือกัน ภิกษุที่มีพรรษามากก็สอนให้ภิกษุที่มีพรรษาน้อย ผู้ไม่ชำนาญในการเย็บจะได้อาศัยผู้ชำนาญ</p>
                                        <p>กฐินมีอยู่ 2 ประเภท คือ<br />
                                        1. กฐินหลวง คือ กฐินที่พระบาทสมเด็จพระเจ้าอยู่หัวเสด็จพระราชดำเนินไปทอดเอง หรือโปรดเกล้าให้พระบรมวงศานุวงศ์ไปแทน หรือ ข้าราชการ ประชาชน ผู้ใดผู้หนึ่ง หรือคณะของพระราชทานผ้ากฐินไปทอด 5 วัดหลวง วัดใดวันหนึ่ง<br />
                                        2. กฐินราษฎร์ คือ กฐินที่ผู้มีจิตศรัทธาผู้ใดผู้หนึ่ง หรือคณะรับจองทอด ณ วัดราษฎร์วัดใดวัดหนึ่ง<br />  </p>      
                         
