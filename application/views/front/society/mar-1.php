                                    <h3>ประเพณีปอยข้าวสังฆ์</h3>
                                    <p>ปอยข้าวสังฆ์ คือ งานทำบุญอุทิศหาผู้ตาย แต่มีลักษณธเป็นงานรื่นเริงอยู่บ้าง คือ มีมหรสพฉลองได้แก่ ซอพื้นเมือง มาซอสมโภชในวันแต่งดา คือ วันเตรียมไทยทานถวายพระสงฆ์ ประเพณีปอยข้าวสังฆ์นี้เป็นงานที่ทำขึ้นด้วยความรักเสน่หาต่อผู้ตายและเป็น ความกตัญญูกตเวทีที่ลูกหลานมีต่อบิดามารดา ปู่ย่าตายายของตน หรือต่อบุคคลผู้เป็นที่รัก เช่น สามีต่อภรรยา และภรรยาต่อสามี</p>
                                    <p>ความนิยมแห่งการทำบุญปอยข้าวสังฆ์ แบ่งออกเป็น 2 ประเภท คือ<br />
                                    1. การทำบุญปอยอุทิศแก่ผู้ชายที่ตายด้วยอุบัติเหตุ<br />
                                    2. การทำบุญปอยอุทิศให้แก่หญิงที่ตายด้วยอุบัติเหตุ และตายทั้งกลม หรือตายด้วยการคลอดบุตร<br />
                                    การทำบุญปอยข้าวสังฆ์แก่ผู้ตายนั้น เจ้าภาพมีวัตถุประสงค์ คือ เพื่อแสดงถึงความรักความผูกพัน เพื่อต้องการให้ดวงวิญญาณไปเกิดในสุคติภพ เพื่อต้องการจะให้เกิดเป็นทิพยสมบัติเกิดแก่ผู้ตายได้เสวยในชาติหน้า เพื่อป้องกันคำครหานินทาว่าทอดทิ้งให้ตายไปตามยถากรรม ทั้งที่มีข้างของสมบัติที่ผู้ตายหาไว้มากมาย เพื่อต้องการให้ผู้ตายพ้นจากวิบากกรรม เพื่ออนุรักษ์มรดกทางวัฒนธรรมดั้งเดิมไว้ มิให้เสื่อมสูญไป บุคคลผู้ที่ตายโดยอุบัติเหตุ (ตายโหง) หรือตายคลอดบุตร คนโบราณถือว่า ผู้ตายอย่างนี้มีบาปกรรมมาก วิญญาณของผู้ตายจะไม่ได้ไปผุดไปเกิด วนเวียนอยู่ในห้วงแห่งกรรม จึงต้องทำบุญอุทิศส่วนกุศลให้เป็นการใหญ่ จึงจะรอดพ้นห่วงกรรมไปได้</p>
                                
