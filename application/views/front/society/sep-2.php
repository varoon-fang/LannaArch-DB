                                    <h3>ประเพณีจาคะข้าว คนเฒ่าจำศีล การจาคะ</h3>
                                      
                                        <p>การจาคะ คือการเสียสละบริจาคข้าวของตนให้เป็นทานแก่ผู้อื่น จาคะข้าว คือถวายข้าวปลาอาหารแด่พระสงฆ์ คนเฒ่าจำศีล คือ อุบาสกอุบาสิกาผู้มีอายุหมดภาระหน้าที่ทางบ้าน จึงพากันเข้าสู่พระศาสนา ประพฤติปฏิบัติถือศีลภาวนา สั่งสมบุญกุศลไว้<br />
                                        ประเพณีจาคะข้าวแก่คนเฒ่าจำศีลนั้น ได้ทำกันมาช้านานแล้วในล้านนาไทย นิยมทำกันในเดือน 12 เหนือ ทั้งนี้เนื่องมาจากเหตุหลายประการ คือ<br />
                                        1. เป็นฤดูกาลฝนตกชุก พระสงฆ์ไปไหนมาไหนไม่สะดวก การทำบุญเป็นการถวายความสะดวกสบายแก่ท่าน<br />
                                        2. เป็นช่วงเวลาที่ข้าวเปลือกหายาก คนผู้มีอันจะกินถือโอกาสช่วยเหลือคนยากจน<br />
                                        3. เป็นช่วงเวลาที่ผู้มีอายุทั้งหลายถืออุโบสถศีลกันเป็นส่วนมาก หากได้ทำทานแก่ท่านเหล่านี้ ถือเป็นบุญกุศลมาก และถือเป็นการสงเคราะห์ด้วย<br />
                                        4. เป็นช่วงเวลาที่คนในครอบครัวอยู่กันพร้อมหน้า เนื่องจากเป็นฤดูฝน การทำกิจกรรมจาคะข้าวจะได้ช่วยกันทำ<br />
                                        5. การทำบุญเดือน 12 ถือว่าจะมีความสมบูรณ์พูนสุข เพราะการเอ่อนองของน้ำฉันใด ถือว่าจะมีบุญกุศลมากอย่างนั้น</p>                 
                         
